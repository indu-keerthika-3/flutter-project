class Flower {
  final String id;
  final String name;
  final String imageUrl;
  final double price;
  final String event;
  final bool isLocal;
  final bool isSeasonal;

  Flower({
    required this.id,
    required this.name,
    required this.imageUrl,
    required this.price,
    required this.event,
    required this.isLocal,
    required this.isSeasonal,
  });
}
